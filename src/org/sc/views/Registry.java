   /**
    * Esse método abre uma caixa de Diálogo para pegar um arquivo
    * 
    * @param title
    *           Título que irá aparecer na caixa de diálogo
    * @param filt
    *           É um array com N filtros utilizados na caixa de diálogo
    * @param tp
    *           É o tipo do arquivo requerido
    * @return a String com o PATH do arquivo
    */
   private String getArchiveDialog(String title, List<FileFilter> filt, int tp)
         throws Exception {
      int retorno;
      String pathReturn = "";
      openFile = new JFileChooser();
      openFile.setDialogTitle(title);
      int i = 0;
      int tamFile = getTamanho(tp);
      while (i < filt.size()) {
         openFile.addChoosableFileFilter(filt.get(i));
         i++;
      }
      retorno = openFile.showOpenDialog(null);
      if (retorno == JFileChooser.APPROVE_OPTION) {
         pathReturn = openFile.getSelectedFile().getAbsolutePath();
         if (!validarArchiveByDialog(pathReturn, tp)) {
            throw new Exception(
                  "Tamanho limite excedido!\nO tamanho máximo do arquivo é de: "
                        + tamFile + "Kb");
         }

      }
      return pathReturn;
   }

   /**
    * Esse método valida o tamanho do arquivo, de acordo com os tamanhos
    * pré-definidos
    * 
    * @param path
    *           é o caminho do arquivo
    * @param type
    *           É o tipo do arquivo(FOTO - 0; DOCUMENTO - 1)
    * @return true, caso seja possível e false c.c.
    */
   private boolean validarArchiveByDialog(String path, int tam) {
      File file = new File(path);
      return !((file.length() / 1024) > getTamanho(tam));
   }

   private int getTamanho(int type) {
      return type == FOTO ? MAX_TAM_FOTO : MAX_TAM_DOCUMENTO;
   }

